        LXI H, 0x0015    ; Загрузка адреса X1
        MOV A, M          ; Чтение X1 в аккумулятор

        INX H             ; Переход к X2 (0x0016)
        ADD M             ; A = X1 + X2
        MOV D, A          ; Сохранить сумму в D

        INX H             ; Переход к X3 (0x0017)
        MOV A, M          ; Чтение X3 в аккумулятор

        INX H             ; Переход к X4 (0x0018)
        SUB M             ; A = X3 - X4
        MOV E, A          ; Сохранить разность в E
        MOV A, D          ; Загрузить сумму X1 + X2
        SUB E             ; Вычислить Y = (X1 + X2) - (X3 - X4)

        INX H             ; Переход к адресу Y (0x0019)
        MOV M, A          ; Сохранить результат в Y
        HLT               ; Остановка процессора

        ORG 0x0015       ; Явное указание адреса данных
        DB 5, 3, 2, 1  ; X1=5, X2=3, X3=2, X4=1

